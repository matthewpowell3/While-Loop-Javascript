///While Loop Javascript
var x = 1;
while (x <= 5) {
  switch (x) {
    case 1:
      console.log('One');
      break;

    case 2:
      console.log('Two');
      break;

    case 3:
      console.log('Three');
      break;

    case 4:
      console.log('Four');
      break;

    case 5:
      console.log('Five');
      break;
  }

  // document.write('<h3>' + x + '</h3>');
  // console.log(x);
  x += 2; // x=x + 2 increment by two
}
